class Colors {
    public static final String RESET = "\u001B[0m";

    // Dark Muted Colors (Same variable names as before)
    public static final String RED = "\u001B[2;31m";       // Dark Red
    public static final String GREEN = "\u001B[2;32m";     // Dark Green
    public static final String YELLOW = "\u001B[2;33m";    // Dark Gold
    public static final String BLUE = "\u001B[2;34m";      // Dark Blue
    public static final String MAGENTA = "\u001B[2;35m";   // Dark Purple
    public static final String CYAN = "\u001B[2;36m";      // Dark Cyan

    // Optional modern UI support
    public static final String GRAY = "\u001B[90m";        // Dark gray
}
